void SC2331_ae_adjust(struct isp_ae_func_cfg *p_cfg)
{
    uint8  i;
    uint8  gain_segment[]     = {1, 2, 4, 8, 16, 32};
    uint8  gain_value[]       = {0, 0x08, 0x09, 0x0b, 0x0f, 0x1f};
    uint8  analog_gain        = (uint8)(p_cfg->analog_gain>>8);
    uint8  sensor_gain_part1  = 0;
    uint16 sensor_gain_part2  = 0;
    uint32 exposure_line      = p_cfg->exposure_line<<1;
    uint8  *addr              = (uint8 *)p_cfg->data.addr;

    for(i=0;i<5;i++){
        if(analog_gain < gain_segment[i+1]){
            sensor_gain_part1 = i;
            break;
        }
    }
    sensor_gain_part2 = (((p_cfg->analog_gain >> sensor_gain_part1) - 256) >> 3) << 2;
    i = 0;
    addr[i++] = 0x3e;
    addr[i++] = 0x02;
    addr[i++] = (exposure_line << 4) & 0xf0;
    addr[i++] = 0x3e;
    addr[i++] = 0x01;
    addr[i++] = (exposure_line >> 4) & 0xff;
    addr[i++] = 0x3e;
    addr[i++] = 0x00;
    addr[i++] = (exposure_line >> 12) & 0x0f;
    addr[i++] = 0x3e;
    addr[i++] = 0x08;
    addr[i++] = gain_value[sensor_gain_part1];
//     // addr[i++] = 0x3e;
//     // addr[i++] = 0x06;
//     // addr[i++] = 0;
    addr[i++] = 0x3e;
    addr[i++] = 0x07;
    addr[i++] = (0x80 + sensor_gain_part2);
    p_cfg->data.size = i;
    p_cfg->cmd_len   = 2+1;
}

